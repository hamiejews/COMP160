public class Customer{
  
/** declaring the variables */
  
  private String name;
    private boolean child;
    private boolean student;
    private boolean booked;
    
/** declares a method which creates an instance of customer  */  
    
    public Customer(String nameIn, int age, boolean studentIn){
    name = nameIn;
    if (5 <= age && age <= 16)
        child = true;
        else
        child = false;
    student = studentIn;
    }
    
/** returns name */
    
    public String getName(){
      return name;
    }

/** returns whether or not is child */    
    
    public boolean isChild(){
      return child;
    }
    
/** returns whether or not is student */    
    
    public boolean isStudent(){
      return student;
    }

/** returns whether or not is booked in */    
    
    public boolean isBooked(){
      return booked;
    }   
    
    public void setBooked(){
    booked = true;
    }
}
